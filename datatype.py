# String
print("Hello World")
print('Hello World')
print('''Hello World''')
print("""Hello World""")

# Si o si necesito el print para mostrar algo en pantalla
print(type('Hello World'))

# Concatenacion de strings
print("Bye" + "World")


# Numbers
print(30)

# enteros(Integer)
print(type(30)) 
# decimal(Float)
print(type(30.5))


# Boolean
True
False
print(type(True))
print(type(False))


# List (es un array)
[10, 20, 30, 55]
['Hello', 'Bye', 'Adios']
[10, 'Hello', True, 10.1]
print(type([10, 20]))

# Tuples (son listas inmutables)
(10, 20, 30, 55)
()
print(type(()))

# Dictionaries (Los objetos)
# Par de clave y valor
{
  "name":"Facundo",
  "apellido": "Prieto",
  "apodo":"Facu"
}

print(type({
  "name":"Facundo",
  "apellido": "Prieto",
  "apodo":"Facu"
}))


# None
None
print(type(None))